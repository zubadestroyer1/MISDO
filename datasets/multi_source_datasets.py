"""
MISDO — Authentic Multi-Sensor Dataset Loaders
==============================================
Loads the actual STAC-downloaded tensors for true domain-specific
environmental risk prediction.

Unlike the synthetic / derived classes, these loaders read distinct
raw sensor arrays:
- Fire: MODIS/VIIRS Active Fire + TerraClimate Vapor Deficit
- Hydro: Copernicus 30m DEM + Hansen Treecover
- Soil: SMAP Soil Moisture + NASA SRTM slope
- Forest: Hansen GFC Multi-temporal stacks
"""

import os
import json
from typing import Tuple

import numpy as np
import torch
from torch import Tensor
from torch.utils.data import Dataset


class TrueMultiSensorBase(Dataset):
    """Base class reading aligned .npz chips generated by STAC downloader."""
    
    def __init__(self, tiles_dir: str = "datasets/true_tiles", split: str = "train", T: int = 5):
        self.tiles_dir = tiles_dir
        self.split = split
        self.T = T
        
        manifest_path = os.path.join(tiles_dir, "manifest.json")
        if not os.path.exists(manifest_path):
            print(f"Warning: {manifest_path} not found. Please run download_earth_data.py")
            self.entries = []
        else:
            with open(manifest_path, "r") as f:
                manifest = json.load(f)
            self.entries = manifest.get(split, [])

    def __len__(self) -> int:
        # Return at least 1 so data loaders don't crash when testing empty structs
        return max(1, len(self.entries))
        
    def _get_dummy_if_missing(self, channels: int) -> Tuple[Tensor, Tensor]:
        """Provides dummy data if actual chips haven't been downloaded yet."""
        obs = torch.zeros((self.T, channels, 256, 256), dtype=torch.float32)
        target = torch.zeros((1, 256, 256), dtype=torch.float32)
        return obs, target


